class whileloo{
    public static void main(String[] args){
        System.out.println("Print numbers 1-100");

        int counter = 0; //prints the name n amout of times 
        while(counter<100){
            System.out.println(counter + "." ); //counter + adds the number while printing
            counter++;
        }
        //System.out.println("Out of loop");
        
    }
}